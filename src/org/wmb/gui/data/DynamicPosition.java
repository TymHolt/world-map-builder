package org.wmb.gui.data;

public class DynamicPosition implements Position {

    public int x;
    public int y;

    public DynamicPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void set(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public int getX() {
        return this.x;
    }

    @Override
    public int getY() {
        return this.y;
    }
}
